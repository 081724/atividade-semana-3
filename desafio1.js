const numero = 10

switch (numero) {
    case 1:
        console.log("segunda")
        break;
    case 2:
        console.log("terca")
        break;
    case 3:
        console.log("quarta")
        break;
    case 4:
        console.log("quinta")
        break;
    case 5:
        console.log("sexta")
        break;
    case 6:
        console.log("sabado")
        break;
    case 7:
        console.log("domingo")
        break;

    default:
      console.log(  numero + " nao representa o dia da semana ")
}